def generate_stop_signal(difficulty: int = 1) -> GameQuestion:
    task = random.choice(_STOP_SIGNAL_TASKS)
    options = task["options"][:]
    random.shuffle(options)

    buttons = [
        [
            InlineKeyboardButton(
                text,
                callback_data=f"game:stop_signal:{'correct' if is_correct else 'wrong'}:{i}",
            )
        ]
        for i, (text, is_correct) in enumerate(options)
    ] + [[InlineKeyboardButton("🏠 Меню", callback_data="menu:main")]]

    text = (
        f"🛑 *СТОП-КРАН ИНТУИЦИИ* — уровень {difficulty}\n"
        f"{'─' * 30}\n\n"
        f"{task['question']}\n\n"
        f"_Выбери ВЕРНОЕ объяснение:_"
    )

    correct_idx = next(i for i, (_, ok) in enumerate(options) if ok)
    return GameQuestion(
        text=text,
        keyboard=InlineKeyboardMarkup(buttons),
        session_data={
            "game_id": "stop_signal",
            "correct_idx": correct_idx,
            "explanation": task["explanation"],
            "started_at": time.time(),
            "difficulty": difficulty,
            "options": options,
        },
    )
