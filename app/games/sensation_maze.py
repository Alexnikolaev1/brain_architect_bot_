def generate_sensation_maze(difficulty: int = 1) -> GameQuestion:
    item = random.choice(_SENSATIONS)
    options = [item["correct"]] + item["wrong"]
    random.shuffle(options)

    buttons = [
        [
            InlineKeyboardButton(
                opt,
                callback_data=f"game:sensation_maze:{'correct' if opt == item['correct'] else 'wrong'}:{i}",
            )
        ]
        for i, opt in enumerate(options)
    ] + [[InlineKeyboardButton("🏠 Меню", callback_data="menu:main")]]

    hint_line = "\n_Закрой глаза и почувствуй..._" if difficulty <= 4 else ""
    text = (
        f"🖐️ *ЛАБИРИНТ ОЩУЩЕНИЙ* — уровень {difficulty}\n"
        f"{'─' * 30}\n\n"
        f"Представь, что ты держишь в руках предмет:\n\n"
        f"*{item['description']}*\n\n"
        f"❓ Что это за предмет?{hint_line}"
    )

    return GameQuestion(
        text=text,
        keyboard=InlineKeyboardMarkup(buttons),
        session_data={
            "game_id": "sensation_maze",
            "correct_answer": item["correct"],
            "explanation": item["explanation"],
            "started_at": time.time(),
            "difficulty": difficulty,
        },
    )
